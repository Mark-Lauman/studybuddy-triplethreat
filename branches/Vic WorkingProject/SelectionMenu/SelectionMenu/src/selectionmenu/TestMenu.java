    private void newPlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newPlayerActionPerformed
        // TODO add your handling code here:
        JOptionPane jo = new JOptionPane();
        String userInput;
        
        userInput = jo.showInputDialog("Please enter the name of the new player");
        try {
        jta1.setText(null);
        File f1 = new File("user");
        if (!f1.exists()){
            f1.mkdir();
        }
        File f2 = new File(f1,"user.txt");
        
        FileReader fr = new FileReader(f2);
        BufferedReader br = new BufferedReader(fr);
        String output;
        
        String line = br.readLine();
        while (line != null){
            output = jta1.getText();
            jta1.setText(output + line + "\n");
            line = br.readLine();
            
        }

        br.close();
        
        FileWriter fw = new FileWriter(f2);
        BufferedWriter bw = new BufferedWriter(fw);
        
        output = jta1.getText() + userInput;
        bw.write(output);
        bw.newLine();
        bw.close();
        
        
        jta1.setText(output);
        }catch(IOException e){
        }
    }//GEN-LAST:event_newPlayerActionPerformed
    
    
