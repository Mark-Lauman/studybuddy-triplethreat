    public String[] getChoices(){
        
        Object tempString[] = choices.getSelectedValues();
        
        String temp2String[] = new String [tempString.length];
        
        //loop all cells tempString
        for(int i = 0; i < tempString.length; i++)
        {
            temp2String[i] = (String) tempString[i];
        }
        //copy them to the temp2String
        
        return temp2String;
        
    }
    
    
